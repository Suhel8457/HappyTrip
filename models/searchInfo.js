class SearchInfo {
    constructor(
        fromCity,
        toCity,
        departureDate,
        arrivalDate,
        numberOfPassengers
    ) {
        this.fromCity = fromCity;
        this.toCity = toCity;
        this.departureDate = departureDate;
        this.arrivalDate = arrivalDate;
        this.numberOfPassengers = numberOfPassengers;
    }
}
